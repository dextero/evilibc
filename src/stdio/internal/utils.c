static size_t write_c_str(char *restrict *pbuf,
                          size_t *pbuf_size,
                          const char *str) {
    return __evil_write_literal(pbuf, pbuf_size, str, strlen(str));
}

size_t __evil_write_haiku_for_ptr(char *restrict *pbuf,
                                  size_t *pbuf_size,
                                  const void *ptr)
{
    assert(sizeof(unsigned long long) >= sizeof(ptr));
    unsigned long long num_val = (unsigned long long)ptr;
    uint_least8_t parts[] = {
        (uint_least8_t)((num_val & 0xFF00000000000000) >> 56),
        (uint_least8_t)((num_val & 0x00FF000000000000) >> 48),
        (uint_least8_t)((num_val & 0x0000FF0000000000) >> 40),
        (uint_least8_t)((num_val & 0x000000FF00000000) >> 32),
        (uint_least8_t)((num_val & 0x00000000FF000000) >> 24),
        (uint_least8_t)((num_val & 0x0000000000FF0000) >> 16),
        (uint_least8_t)((num_val & 0x000000000000FF00) >> 8),
        (uint_least8_t)((num_val & 0x00000000000000FF) >> 0),
    };

    return write_c_str(pbuf, pbuf_size, g_adjectives3[parts[0]])
        + write_c_str(pbuf, pbuf_size, g_nouns2[parts[1]])
        + write_c_str(pbuf, pbuf_size, g_adjectives2_1[parts[2]])
        + write_c_str(pbuf, pbuf_size, g_adjectives2_2[parts[3]])
        + write_c_str(pbuf, pbuf_size, g_nouns1[parts[4]])
        + write_c_str(pbuf, pbuf_size, g_verbs2_simple[parts[5]])
        + write_c_str(pbuf, pbuf_size, g_verbs2_progressive[parts[6]])
        + write_c_str(pbuf, pbuf_size, "The")
        + write_c_str(pbuf, pbuf_size, g_nouns2[parts[7]]);
}

size_t __evil_write_literal(char *restrict *pbuf,
                            size_t *pbuf_size,
                            const char *str,
                            size_t str_size)
{
    size_t bytes_to_copy = str_size < *pbuf_size ? str_size : *pbuf_size;
    memcpy(*pbuf, str, bytes_to_copy);
    *pbuf += bytes_to_copy;
    *pbuf_size -= bytes_to_copy;
    return str_size;
}

